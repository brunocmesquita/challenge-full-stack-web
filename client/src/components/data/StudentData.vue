<template>
  <div>
    <template>
      <v-alert
        v-if="error != undefined"
        class="font-weight-bold red--text red lighten-4"
      >
        {{ error }}
      </v-alert>
      <v-data-table
        :headers="headers"
        :items="users"
        sort-by="calories"
        class="elevation-1"
      >
        <template v-slot:top>
          <v-toolbar flat>
            <v-toolbar-title>Challenge Alunos</v-toolbar-title>
            <v-divider class="mx-4" inset vertical></v-divider>
            <v-spacer></v-spacer>
            <v-dialog v-model="dialog" max-width="500px">
              <template v-slot:activator="{ on, attrs }">
                <v-btn
                  color="primary"
                  dark
                  class="mb-2"
                  v-bind="attrs"
                  v-on="on"
                >
                  Cadastrar aluno
                </v-btn>
              </template>
              <v-card>
                <v-card-title>
                  <span class="text-h5">{{ formTitle }}</span>
                </v-card-title>

                <v-card-text>
                  <v-container>
                    <v-row>
                      <v-col cols="12" sm="6" md="4">
                        <v-text-field
                          :disabled="editedIndex !== -1"
                          v-model="editedItem.ra_aluno"
                          label="RA"
                        ></v-text-field>
                      </v-col>
                      <v-col cols="12" sm="6" md="4">
                        <v-text-field
                          v-model="editedItem.name"
                          label="Nome"
                        ></v-text-field>
                      </v-col>
                      <v-col cols="12" sm="6" md="4">
                        <v-text-field
                          v-model="editedItem.cpf"
                          label="CPF"
                        ></v-text-field>
                      </v-col>
                      <v-col cols="12" sm="6" md="4">
                        <v-text-field
                          v-model="editedItem.email"
                          label="E-mail"
                        ></v-text-field>
                      </v-col>
                    </v-row>
                  </v-container>
                </v-card-text>

                <v-card-actions>
                  <v-spacer></v-spacer>
                  <v-btn color="blue darken-1" text @click="close">
                    Cancel
                  </v-btn>
                  <v-btn color="blue darken-1" text @click="save">
                    Save
                  </v-btn>
                </v-card-actions>
              </v-card>
            </v-dialog>
            <v-dialog v-model="dialogDelete" max-width="500px">
              <v-card>
                <v-card-title class="text-h5"
                  >Are you sure you want to delete this item?</v-card-title
                >
                <v-card-actions>
                  <v-spacer></v-spacer>
                  <v-btn color="blue darken-1" text @click="closeDelete"
                    >Cancel</v-btn
                  >
                  <v-btn color="blue darken-1" text @click="deleteItemConfirm"
                    >OK</v-btn
                  >
                  <v-spacer></v-spacer>
                </v-card-actions>
              </v-card>
            </v-dialog>
          </v-toolbar>
        </template>
        <template v-slot:item.actions="{ item }">
          <v-icon small class="mr-2" @click="editItem(item)">
            mdi-pencil
          </v-icon>
          <v-icon small @click="deleteItem(item)">
            mdi-delete
          </v-icon>
        </template>
        <template v-slot:no-data>
          <v-btn color="primary" @click="initialize">
            Reset
          </v-btn>
        </template>
      </v-data-table>
    </template>
  </div>
</template>

<script>
import axios from 'axios';
export default {
  created() {
    this.initialize();
  },
  data() {
    return {
      dialog: false,
      error: undefined,
      dialogDelete: false,
      headers: [
        {
          text: 'RA',
          align: 'start',
          sortable: false,
          value: 'ra_aluno',
        },
        { text: 'Nome', value: 'name' },
        { text: 'CPF', value: 'cpf' },
        { text: 'E-mail', value: 'email' },
        { text: 'Actions', value: 'actions', sortable: false },
      ],
      editedIndex: -1,
      editedItem: {
        name: '',
        cpf: '',
        email: '',
      },
      defaultItem: {
        name: '',
        cpf: '',
        email: '',
      },
      users: [],
    };
  },
  computed: {
    formTitle() {
      return this.editedIndex === -1 ? 'Cadastrar aluno' : 'Editar aluno';
    },
  },
  watch: {
    dialog(val) {
      val || this.close();
    },
    dialogDelete(val) {
      val || this.closeDelete();
    },
  },
  methods: {
    initialize() {
      axios
        .get('http://localhost:3333/api/users')
        .then(res => (this.users = res.data.data));
    },
    editItem(item) {
      this.editedIndex = this.users.indexOf(item);
      this.editedItem = Object.assign({}, item);
      this.dialog = true;
    },
    deleteItem(item) {
      this.editedIndex = this.users.indexOf(item);
      this.editedItem = Object.assign({}, item);
      this.dialogDelete = true;
    },
    deleteItemConfirm() {
      this.users.splice(this.editedIndex, 1);
      this.closeDelete();
    },
    close() {
      this.dialog = false;
      this.$nextTick(() => {
        this.editedItem = Object.assign({}, this.defaultItem);
        this.editedIndex = -1;
      });
    },
    closeDelete() {
      this.dialogDelete = false;
      this.$nextTick(() => {
        axios
          .delete(`http://localhost:3333/api/users/${this.editedItem.id}`)
          .then(() => this.$store.dispatch('deleteUser'));
      });
    },
    save() {
      if (this.formTitle === 'Editar aluno') {
        if (this.editedIndex > -1) {
          axios
            .put(`http://localhost:3333/api/users/${this.editedItem.id}`, {
              name: this.editedItem.name,
              cpf: this.editedItem.cpf,
              email: this.editedItem.email,
            })
            .then(() => {
              this.$store.dispatch('updateUser');
              this.initialize();
              this.close();
            })
            .catch(err => {
              let msgErro = err.message;
              this.error = msgErro;
            });
        } else {
          this.users.push(this.editedItem);
        }
        this.close();
      }
      if (this.formTitle === 'Cadastrar aluno') {
        axios
          .post('http://localhost:3333/api/users', {
            ra_aluno: this.editedItem.ra_aluno,
            name: this.editedItem.name,
            cpf: this.editedItem.cpf,
            email: this.editedItem.email,
          })
          .then(() => {
            this.$store.dispatch('createUser');
            this.initialize();
            this.close();
          })
          .catch(err => console.log(err));
      }
    },
  },
};
</script>

